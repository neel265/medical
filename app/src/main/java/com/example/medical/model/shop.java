package com.example.medical.model;

public class shop {
    int hah;
    String tas;

    public shop(int hah, String tas) {
        this.hah = hah;
        this.tas = tas;
    }

    public int getHah() {
        return hah;
    }

    public void setHah(int hah) {
        this.hah = hah;
    }

    public String getTas() {
        return tas;
    }

    public void setTas(String tas) {
        this.tas = tas;
    }
}
