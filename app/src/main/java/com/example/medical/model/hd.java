package com.example.medical.model;

public class hd {
    int iah;
    String tah;

    public hd(int iah, String tah) {
        this.iah = iah;
        this.tah = tah;
    }

    public int getIah() {
        return iah;
    }

    public void setIah(int iah) {
        this.iah = iah;
    }

    public String getTah() {
        return tah;
    }

    public void setTah(String tah) {
        this.tah = tah;
    }
}
