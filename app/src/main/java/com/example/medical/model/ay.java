package com.example.medical.model;

public class ay
{
    int ia;
    String ta;

    public ay(int ia, String ta) {
        this.ia = ia;
        this.ta = ta;
    }

    public int getIa() {
        return ia;
    }

    public void setIa(int ia) {
        this.ia = ia;
    }

    public String getTa() {
        return ta;
    }

    public void setTa(String ta) {
        this.ta = ta;
    }
}
