package com.example.medical.model;

public class home
{
    int imh;
    String tmh;

    public home(int imh, String tmh) {
        this.imh = imh;
        this.tmh = tmh;
    }

    public int getImh() {
        return imh;
    }

    public void setImh(int imh) {
        this.imh = imh;
    }

    public String getTmh() {
        return tmh;
    }

    public void setTmh(String tmh) {
        this.tmh = tmh;
    }
}
