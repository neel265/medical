package com.example.medical.model;

public class dc
{
    int iad;
    String tad;

    public dc(int iad, String tad) {
        this.iad = iad;
        this.tad = tad;
    }

    public int getIad() {
        return iad;
    }

    public void setIad(int iad) {
        this.iad = iad;
    }

    public String getTad() {
        return tad;
    }

    public void setTad(String tad) {
        this.tad = tad;
    }
}
