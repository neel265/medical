package com.example.medical.model;

public class skin {
    int ims;
    String tms;

    public skin(int ims, String tms) {
        this.ims = ims;
        this.tms = tms;
    }

    public int getIms() {
        return ims;
    }

    public void setIms(int ims) {
        this.ims = ims;
    }

    public String getTms() {
        return tms;
    }

    public void setTms(String tms) {
        this.tms = tms;
    }
}
